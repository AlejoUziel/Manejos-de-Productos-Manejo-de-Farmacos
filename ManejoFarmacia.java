    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        // Obtener los datos de los campos de texto
    String nombre = txtnombre.getText();
    String codigo = txtCodigo.getText();
    String cantidad = txtCantidadDisponible.getText();
    String precio = txtPrecio.getText();
    
    // Calcular el total (Cantidad * Precio)
    double total = 0;
    try {
        int cantidadDisponible = Integer.parseInt(cantidad);
        double precioUnitario = Double.parseDouble(precio);
        total = cantidadDisponible * precioUnitario;
    } catch (NumberFormatException e) {
        // Manejar la excepción si el formato del número es incorrecto
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, ingrese valores válidos para la cantidad y el precio.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Agregar los datos a la tabla
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tableFarmacos.getModel();
    model.addRow(new Object[]{nombre, codigo, cantidad, precio, total});
    
    // Limpiar los campos de texto
    txtnombre.setText("");
    txtCodigo.setText("");
    txtCantidadDisponible.setText("");
    txtPrecio.setText("");
    }//GEN-LAST:event_btnAgregarActionPerformed

