    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
         int selectedRow = tableFarmacos.getSelectedRow();
    if (selectedRow >= 0) {
        // Eliminar la fila seleccionada
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tableFarmacos.getModel();
        model.removeRow(selectedRow);
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila para eliminar.", "Error", javax.swing.JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

